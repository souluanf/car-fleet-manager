(
    id   BINARY(16) PRIMARY KEY DEFAULT (UNHEX(REPLACE(UUID(), '-', ''))),
    name VARCHAR(100) NOT NULL UNIQUE
);

CREATE INDEX idx_brands_name ON brands (name);

